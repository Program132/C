#include <iostream>

using namespace std;

int add(int a, int b)
{
	return a + b;
}

int remove(int a, int b)
{
	return a - b;
}

int divi(int a, int b)
{
	return a / b;
}

int multi(int a, int b)
{
	return a * b;
}

int doubleNbr(int nbr)
{
	return nbr * nbr;
}

int tripleNbr(int nbr)
{
	return nbr * nbr * nbr;
}